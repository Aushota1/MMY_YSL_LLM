def filter_by_length(qa_pairs: List[Tuple[str, str]], min_words: int = 15, max_words: int = 50) -> List[Tuple[str, str]]:
    """Фильтрация пар вопрос-ответ по длине текста"""
    filtered = []
    for question, answer in qa_pairs:
        combined_text = f"{question} {answer}"
        word_count = count_words(combined_text)
        # Более мягкая фильтрация - принимаем примеры от 10 до 60 слов
        if 10 <= word_count <= 60:
            filtered.append((question, answer))
    return filtered


def create_tokenizer_dataset(qa_pairs: List[Tuple[str, str]]) -> List[str]:
    """
    Создание датасета для токенизатора
    Объединяет вопросы и ответы в простой текст
    """
    texts = []
    for question, answer in qa_pairs:
        # Объединяем вопрос и ответ в один текст
        combined = f"{question} {answer}"
        cleaned = clean_text(combined)
        if cleaned:
            texts.append(cleaned)
    return texts


def create_transformer_dataset(qa_pairs: List[Tuple[str, str]]) -> List[str]:
    """
    Создание датасета для TRANSFORMER
    Формат: Q: вопрос A: ответ (в одной строке)
    """
    formatted_texts = []
    for question, answer in qa_pairs:
        question_cleaned = clean_text(question)
        answer_cleaned = clean_text(answer)
        if question_cleaned and answer_cleaned:
            # Объединяем вопрос и ответ в одну строку
            formatted = f"Q: {question_cleaned} A: {answer_cleaned}"
            formatted_texts.append(formatted)
    return formatted_texts


def save_dataset(texts: List[str], filepath: str):
    """Сохранение датасета в файл"""
    os.makedirs(os.path.dirname(filepath), exist_ok=True)
    with open(filepath, 'w', encoding='utf-8') as f:
        for text in texts:
            f.write(text + '\n')
    print(f"✓ Сохранено {len(texts)} примеров в {filepath}")


